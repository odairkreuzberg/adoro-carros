package br.iav.ac.negocio;

public class Atividade {
 
	private String nome;
	private String tipo;
	private ListaPeca listaPeca;

	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getTipo() {
		return tipo;
	}
	
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	public ListaPeca getListaPeca() {
		return listaPeca;
	}
	
	public void setListaPeca(ListaPeca listaPeca) {
		this.listaPeca = listaPeca;
	}

}
 
