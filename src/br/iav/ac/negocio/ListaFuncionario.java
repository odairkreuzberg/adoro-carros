package br.iav.ac.negocio;

public class ListaFuncionario {

	private Funcionario funcionario;
	private Funcionario proxFuncionario;
	
	public Funcionario getFuncionario() {
		return funcionario;
	}
	
	public void setFuncionario(Funcionario funcionario) {
		this.funcionario = funcionario;
	}
	
	public Funcionario getProxFuncionario() {
		return proxFuncionario;
	}
	
	public void setProxFuncionario(Funcionario proxFuncionario) {
		this.proxFuncionario = proxFuncionario;
	}

}
 
