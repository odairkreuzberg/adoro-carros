package br.iav.ac.negocio;

public class Pais {

	private String nome;
	private int ddi;
	private Estado estado;
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public int getDdi() {
		return ddi;
	}
	
	public void setDdi(int ddi) {
		this.ddi = ddi;
	}
	
	public Estado getEstado() {
		return estado;
	}
	
	public void setEstado(Estado estado) {
		this.estado = estado;
	}

}
 
